package edunova.zadatak2;

public class Vozilo {
	
	int masa;
	int maksimalnaBrzina;
	int snaga;
	
	
	public Vozilo(int masa, int maksimalnaBrzina, int snaga) {
		this.masa = masa;
		this.maksimalnaBrzina = maksimalnaBrzina;
		this.snaga = snaga;
	}


	public int getMasa() {
		return masa;
	}


	public void setMasa(int masa) {
		this.masa = masa;
	}


	public int getMaksimalnaBrzina() {
		return maksimalnaBrzina;
	}


	public void setMaksimalnaBrzina(int maksimalnaBrzina) {
		this.maksimalnaBrzina = maksimalnaBrzina;
	}


	public int getSnaga() {
		return snaga;
	}


	public void setSnaga(int snaga) {
		this.snaga = snaga;
	}
	

	

	

}
